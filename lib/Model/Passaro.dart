// Construtor base

class Passaro {
  String nome;
  String imagem;
  String NC;
  String familia;
  String peso;
  String tamanho;
  String descricao;


  Passaro(String nome, String imagem, String NC, String familia,
      String peso, String tamanho, String descricao)
  {
    this.nome = nome;
    this.imagem = imagem;
    this.NC = NC;
    this.familia = familia;
    this.peso = peso;
    this.tamanho = tamanho;
    this.descricao = descricao;

  }

}
